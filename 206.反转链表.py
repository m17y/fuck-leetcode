# @lc code=start
# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution(object):
    def reverseList(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        pre = None

        while head:
            tmp = head
            tmp.next = pre
            pre = tmp
            head = head.next 
        return pre.next

# @lc code=end

